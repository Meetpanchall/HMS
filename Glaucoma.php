                                    <h2>Glaucoma</h2>
                                    <p>Glaucoma is the most serious eyesight threatening condition. It usually manifests as a painless, gradual loss of vision. The lost vision can never be recovered. However, medical or surgical treatment can prevent or retard further loss of vision.</p>
                                    <p>Many times it can be confused with a cataract which also manifests as a painless, gradual loss of vision. The difference is that in the case of cataract, the loss of vision is fully recoverable using a simple surgery called Phaco.
Our eyes contain a clear fluid called aqueous humor, which is continuously produced in the eye to bath and nourish the structures inside it. The fluid normally drains out of the eye through drainage canals in a fine meshwork located around the edge of the iris (the colored part of the eye that surrounds the pupil). In people with glaucoma, the fluid fails to drain due to some defect and thus increases the pressure inside the eyes called raised Intraocular Pressure (IOP) (or Tension).
