                                    <h2>Cataract</h2>
                                    <p>Cataract is caused by the clouding of the natural lens of the eye and usually develops as a result of old age, but may occur in infants and young children too. This results in blurred vision, glare, and sensitivity to bright lights. As the disease progresses, it leads to painless, progressive decrease in vision which can also result in a poor quality of life, interfering with day-to-day activities like driving and reading.</p>
